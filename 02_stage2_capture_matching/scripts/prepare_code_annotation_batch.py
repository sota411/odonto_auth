@dataclass(frozen=True)
class Checkup:
    split: str
    patient_id: str
    checkup_id: str
    photographs: tuple[str, ...]
    source_row_number: int

    @property
    def uid(self) -> str:
        return f"{self.patient_id}:{self.checkup_id}"


@dataclass(frozen=True)
class SelectedPhoto:
    split: str
    image_name: str
    patient_token: str
    checkup_token: str
    source_patient_id: str
    source_checkup_id: str
    source_row_number: int
    source_photo_reference: str
    source_path: Path
    source_sha256: str


def find_project_root(start: Path) -> Path:
    for path in (start, *start.parents):
        if (path / "pyproject.toml").exists():
            return path
    raise RuntimeError(f"project root not found from: {start}")


def validate_output_location(output_dir: Path, repo_root: Path) -> None:
    resolved_output = output_dir.resolve()
    resolved_repo = repo_root.resolve()
    if not resolved_output.is_relative_to(resolved_repo):
        return

    relative_output = resolved_output.relative_to(resolved_repo)
    tracked_result = subprocess.run(
        ["git", "ls-files", "--cached", "--", relative_output.as_posix()],
        cwd=resolved_repo,
        check=False,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
    )
    if tracked_result.returncode != 0:
        raise RuntimeError(
            f"failed to inspect tracked output files: {tracked_result.stderr.strip()}"
        )
    if tracked_result.stdout.strip() != "":
        raise RuntimeError(
            f"output directory contains tracked files and cannot store private data: "
            f"{resolved_output}"
        )

    result = subprocess.run(
        ["git", "check-ignore", "--quiet", "--no-index", str(resolved_output)],
        cwd=resolved_repo,
        check=False,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.PIPE,
        text=True,
    )
    if result.returncode == 0:
        return
    if result.returncode == 1:
        raise RuntimeError(
            f"output directory inside the repository must be Git-ignored: {resolved_output}"
        )
    raise RuntimeError(
        f"failed to verify whether output directory is Git-ignored: {result.stderr.strip()}"
    )


